package com.paragon.visitorchek.model;

public class ApartmentRequest {
	
	private String id;	
	private String mobileNumber;
	private String blockNo;
	private String houseNo;
	private String ownerTenant;
	private String image;
	private String talentId;
	private String loginTalentId;
	private String residentPersonName;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getMobileNumber() {
		return mobileNumber;
	}
	public void setMobileNumber(String mobileNumber) {
		this.mobileNumber = mobileNumber;
	}
	public String getBlockNo() {
		return blockNo;
	}
	public void setBlockNo(String blockNo) {
		this.blockNo = blockNo;
	}
	public String getHouseNo() {
		return houseNo;
	}
	public void setHouseNo(String houseNo) {
		this.houseNo = houseNo;
	}
	public String getOwnerTenant() {
		return ownerTenant;
	}
	public void setOwnerTenant(String ownerTenant) {
		this.ownerTenant = ownerTenant;
	}
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
	public String getTalentId() {
		return talentId;
	}
	public void setTalentId(String talentId) {
		this.talentId = talentId;
	}
	public String getLoginTalentId() {
		return loginTalentId;
	}
	public void setLoginTalentId(String loginTalentId) {
		this.loginTalentId = loginTalentId;
	}
	public String getResidentPersonName() {
		return residentPersonName;
	}
	public void setResidentPersonName(String residentPersonName) {
		this.residentPersonName = residentPersonName;
	}

}
