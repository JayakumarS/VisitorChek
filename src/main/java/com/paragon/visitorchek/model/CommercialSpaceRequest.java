package com.paragon.visitorchek.model;

public class CommercialSpaceRequest {

	
	private String id;
	private String loginTalentId;
	private String mobileNumber;
	private String blockNo;
	private String adminTalentId;
	private String talentId;
	private String image;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getLoginTalentId() {
		return loginTalentId;
	}
	public void setLoginTalentId(String loginTalentId) {
		this.loginTalentId = loginTalentId;
	}
	public String getMobileNumber() {
		return mobileNumber;
	}
	public void setMobileNumber(String mobileNumber) {
		this.mobileNumber = mobileNumber;
	}
	public String getBlockNo() {
		return blockNo;
	}
	public void setBlockNo(String blockNo) {
		this.blockNo = blockNo;
	}
	public String getAdminTalentId() {
		return adminTalentId;
	}
	public void setAdminTalentId(String adminTalentId) {
		this.adminTalentId = adminTalentId;
	}
	public String getTalentId() {
		return talentId;
	}
	public void setTalentId(String talentId) {
		this.talentId = talentId;
	}
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
	
}
